(function () {
  const form = document.querySelector('.contact-form');
  if (!form) return;

  const demandSelect = document.getElementById('tipo-demanda');
  const propertyFields = document.getElementById('property-fields');
  const frontageInput = document.getElementById('metragem-frente');
  const depthInput = document.getElementById('metragem-fundo');
  const totalAreaInput = document.getElementById('area-total');
  const statusBox = document.getElementById('form-status');
  const submitButton = form.querySelector('button[type="submit"]');
  const propertyInputs = propertyFields ? propertyFields.querySelectorAll('input, textarea, select') : [];
  const propertyDemandValues = new Set(['Intermediação de terreno/imóvel', 'Intermediação de imóvel', 'Intermediação de terreno']);

  function setStatus(type, message) {
    if (!statusBox) return;
    statusBox.className = 'form-status is-visible ' + (type === 'success' ? 'is-success' : 'is-error');
    statusBox.textContent = message;
  }

  function clearStatus() {
    if (!statusBox) return;
    statusBox.className = 'form-status';
    statusBox.textContent = '';
  }

  function togglePropertyFields() {
    const enabled = propertyDemandValues.has(demandSelect.value);
    if (!propertyFields) return;
    propertyFields.hidden = !enabled;
    propertyInputs.forEach((input) => {
      input.disabled = !enabled;
      if (!enabled && input !== totalAreaInput) input.value = '';
    });
    if (!enabled && totalAreaInput) totalAreaInput.value = '';
  }

  function calculateArea() {
    if (!frontageInput || !depthInput || !totalAreaInput) return;
    const frontage = parseFloat(String(frontageInput.value).replace(',', '.'));
    const depth = parseFloat(String(depthInput.value).replace(',', '.'));
    if (Number.isFinite(frontage) && Number.isFinite(depth) && frontage > 0 && depth > 0) {
      totalAreaInput.value = (frontage * depth).toFixed(2).replace('.', ',');
    } else {
      totalAreaInput.value = '';
    }
  }

  demandSelect?.addEventListener('change', () => {
    togglePropertyFields();
    calculateArea();
  });
  frontageInput?.addEventListener('input', calculateArea);
  depthInput?.addEventListener('input', calculateArea);
  togglePropertyFields();

  form.addEventListener('submit', async (event) => {
    event.preventDefault();
    clearStatus();

    const formData = new FormData(form);
    if ((formData.get('website') || '').toString().trim() !== '') {
      setStatus('success', 'Solicitação recebida.');
      form.reset();
      togglePropertyFields();
      return;
    }

    if (submitButton) {
      submitButton.disabled = true;
      submitButton.dataset.originalText = submitButton.textContent;
      submitButton.textContent = 'Enviando...';
    }

    try {
      const response = await fetch('/api/contact', {
        method: 'POST',
        headers: {
          'Accept': 'application/json'
        },
        body: formData
      });

      const data = await response.json().catch(() => ({}));
      if (!response.ok) {
        throw new Error(data.error || 'Não foi possível enviar o formulário agora.');
      }

      setStatus('success', data.message || 'Mensagem enviada com sucesso. A Delta retornará pelo e-mail informado.');
      form.reset();
      togglePropertyFields();
      calculateArea();
    } catch (error) {
      setStatus('error', error.message || 'Falha ao enviar. Revise as configurações do provedor de e-mail no Cloudflare Pages.');
    } finally {
      if (submitButton) {
        submitButton.disabled = false;
        submitButton.textContent = submitButton.dataset.originalText || 'Enviar solicitação';
      }
    }
  });
})();
